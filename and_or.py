# AND or OR operators:
#AND Operator:
name = "Abhishek"
age = 24
if name=="Abhishek" and age==24: #Both conditions have to be true in AND operator.Only then it prints condition True.
    print("condition True")
else:
    print("condition False")

#OR Operator:

if name=="Abhishek" or age==24:  #If any one condition is true then it prints condition True.
    print("condition True")
else:
    print("condition False")