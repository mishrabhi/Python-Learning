# You can use single quotes or double quotes,Both will do the same.
print("hello world") 
print('hello world')  
# You can use 'single quotes' inside "double quotes" OR You can use "double quotes" inside 'single quotes'.
print("Hello 'world' World")
print('Hello "world" World')

