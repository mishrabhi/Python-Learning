# Step Argument:
# for i in range(start argument,end argument,step)
# for i in range(1,11):
#     print(i)

for i in range(1,11,2):   # this gives you 1-1 gap between numbers or you can say it prints odd numbers between 1 to 10.
    print(i)

for i in range(0,11,2):  # It gives you even numbers between o to 10
    print(i)


# Basically we use step arguments to give steps or gap between what we are printing.