# For-Loop
# for loop with range function
# We are going to print "Hello World" 10 times using for loop range function

for i in range(10):         # You can give range in range function
    print("Hello World")    # It prints Hello World 10 times

for i in range(1, 11):
    print(f"Hello World : {i}")      # It prints Hello World : 1 to Hello World : 10 